void VerificarChk(unsigned char *T, unsigned char tam, unsigned char i, unsigned char omitir){
	//T[14] a T[14+IHL-1] y tam = 14 + IHL - 1
	unsigned int chk = 0;
	
	while(i<=tam){
		if(!(i == omitir))
			chk += (T[i] << 8) | T[i | 1];
		i+=2;	
	}		
		
	chk = (~((chk&65535) | (chk >> 16)))&65535;
	
	if(chk == ((T[omitir] << 8) | T[omitir+1])){
		printf("El Checksum 0x%.4x es correcto :)", (T[omitir] << 8) | T[omitir+1]);
	}
	else{
		printf("El Checksum 0x%.4x no es correcto :( \n", (T[omitir] << 8) | T[omitir+1]);
		printf("El Checksum calculado es: 0x%.4x\n", chk);
	}	
}

