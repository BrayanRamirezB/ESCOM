void AnalizaTrama(unsigned char *T){
	
	printf("\n.::Cabecera Ethernet::.\n");
	printf("MAC Destino: %.2x: %.2x: %.2x: %.2x: %.2x: %.2x\n", T[0], T[1], T[2], T[3], T[4], T[5]);
	printf("MAC Origen: %.2x: %.2x: %.2x: %.2x: %.2x: %.2x\n\n", T[6], T[7], T[8], T[9], T[10], T[11]);
	
	unsigned short tot = (T[12]<<8)|T[13];
	
	if(tot < 1500){
		printf("Tamano de la cabecera LLC: %d bytes\n", tot);
		AnalizaLLC(T);
	}
	else{
		if(tot == 2048){
			AnalizaIP(T);
		}else if(tot == 2054){
			AnalizaARP(T);
		}else{
			printf("Otro %2.x %2.x\n", T[12], T[13]);
		}
	}
			
}

